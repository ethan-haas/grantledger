export function MobileNav() {
  const pathname = usePathname();
  const { setSidebarOpen } = useUiStore();

  function handleSearchClick(e: React.MouseEvent) {
    e.preventDefault();
    // Dispatch Cmd+K to open command palette
    const event = new KeyboardEvent("keydown", {
      key: "k",
      code: "KeyK",
      metaKey: true,
      ctrlKey: true,
      bubbles: true,
    });
    document.dispatchEvent(event);
  }

  return (
    <nav
      aria-label="Mobile navigation"
      className="fixed bottom-0 left-0 right-0 z-sidebar border-t border-slate-200 bg-white md:hidden dark:border-slate-700 dark:bg-slate-900"
    >
      <div className="flex items-center justify-around px-2 py-2">
        {mobileNavItems.map((item) => {
          const isSearch = "isSearch" in item && item.isSearch;
          const isActive = !isSearch && (
            item.href === "/dashboard"
              ? pathname === "/dashboard"
              : pathname.startsWith(item.href)
          );

          if (isSearch) {
            return (
              <button
                key="search"
                type="button"
                onClick={handleSearchClick}
                className="flex flex-col items-center gap-0.5 rounded-lg px-3 py-2 min-h-[44px] min-w-[44px] text-slate-500 transition-colors hover:text-slate-700 dark:text-slate-400 dark:hover:text-slate-200"
                aria-label="Open search"
              >
                {item.icon}
              </button>
            );
          }

          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setSidebarOpen(false)}
              className={`flex flex-col items-center gap-0.5 rounded-lg px-3 py-2 min-h-[44px] min-w-[44px] transition-colors ${
                isActive
                  ? "text-primary-600 dark:text-primary-400"
                  : "text-slate-500 hover:text-slate-700 dark:text-slate-400 dark:hover:text-slate-200"
              }`}
            >
              {item.icon}
              {isActive && (
                <span className="text-[10px] font-medium">{item.label}</span>
              )}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
